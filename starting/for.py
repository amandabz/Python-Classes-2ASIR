# for

fruits = ["apple", "banana", "cherry", "cherry", "kiwi", "mango"]
newlist = []

for fruit in fruits:
    if "a" in fruit:
        newlist.append(fruit)

print(newlist)  # printea la nueva lista con las frutas que contengan una "a" en su nombre


# print del 1 al 5 (longitud de la lista)
