import math

# operators

# % : te da el resto de la división
x = 46
y = 2

print(x % y) 

# // : hace la divsión y redondea hacia abajo
x = 15
y = 2

print(x // y) 

# += 
x = 5
x += 3  # es lo mismo que x = x + 3

print(x)

# ceil = redondea para arriba
# floor = redondea para abajo
x = math.ceil(1.4)
y = math.floor(1.4)

print(x)  # devuelve 2
print(y)  # devuelve 1
